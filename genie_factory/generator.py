# Validation
# ---------------------------------------------------------------------------


def _validate_domain_spec(
    spec: DomainSpec,
    expected_tables: int,
    expected_products: int,
    expected_locations: int,
) -> None:
    """Validate that the domain spec meets constraints."""

    if len(spec.tables) != expected_tables:
        raise ValueError(
            f"Expected {expected_tables} tables, got {len(spec.tables)}"
        )

    if len(spec.metric_views) != expected_tables:
        raise ValueError(
            f"Expected {expected_tables} metric views, got {len(spec.metric_views)}"
        )

    if len(spec.sample_questions) != 7:
        raise ValueError(
            f"Expected 7 sample questions, got {len(spec.sample_questions)}"
        )

    if len(spec.example_sqls) != 7:
        raise ValueError(
            f"Expected 7 example SQLs, got {len(spec.example_sqls)}"
        )

    if len(spec.benchmarks) != 7:
        raise ValueError(
            f"Expected 7 benchmarks, got {len(spec.benchmarks)}"
        )

    if len(spec.sql_snippets.filters) != 3:
        raise ValueError(
            f"Expected 3 filters, got {len(spec.sql_snippets.filters)}"
        )

    if len(spec.sql_snippets.expressions) != 3:
        raise ValueError(
            f"Expected 3 expressions, got {len(spec.sql_snippets.expressions)}"
        )

    if len(spec.sql_snippets.measures) != 4:
        raise ValueError(
            f"Expected 4 measures, got {len(spec.sql_snippets.measures)}"
        )

    # Verify 3-5 example SQLs use MEASURE() (target: 4 MEASURE / 3 plain table)
    measure_examples = sum(
        1 for ex in spec.example_sqls
        if any("MEASURE(" in line for line in ex.sql_lines)
    )
    if measure_examples < 3:
        raise ValueError(
            f"At least 3 example SQLs must use MEASURE(), found {measure_examples}"
        )
    if measure_examples > 5:
        raise ValueError(
            f"At most 5 example SQLs should use MEASURE(), found {measure_examples}"
        )

    # Verify 3-5 benchmarks use MEASURE() (target: 4 MEASURE / 3 plain table)
    measure_benchmarks = sum(
        1 for b in spec.benchmarks
        if any("MEASURE(" in line for line in b.sql_lines)
    )
    if measure_benchmarks < 3:
        raise ValueError(
            f"At least 3 benchmarks must use MEASURE(), found {measure_benchmarks}"
        )
    if measure_benchmarks > 5:
        raise ValueError(
            f"At most 5 benchmarks should use MEASURE(), found {measure_benchmarks}"
        )

    # Verify metric view measures contain exactly one aggregate function.
    # Metric views only allow a single aggregate per measure — ratios like
    # SUM(x)/COUNT(y) must be split into separate measures.
    _AGG_FUNCS = ("SUM(", "COUNT(", "AVG(", "MIN(", "MAX(", "STDDEV(", "VARIANCE(")
    for mv in spec.metric_views:
        for m in mv.measures:
            expr = m.get("expr", "").upper()
            agg_count = sum(expr.count(func) for func in _AGG_FUNCS)
            if agg_count == 0:
                raise ValueError(
                    f"Metric view '{mv.view_name}' measure '{m.get('name')}' "
                    f"has no aggregate function: {m.get('expr')}"
                )
            if agg_count > 1:
                raise ValueError(
                    f"Metric view '{mv.view_name}' measure '{m.get('name')}' "
                    f"contains {agg_count} aggregate calls but metric views allow "
                    f"exactly one per measure. Split ratio measures into separate "
                    f"measures. Expression: {m.get('expr')}"
                )

    # Validate metric view dimension/measure references against source table columns
    table_columns_map = {
        t.table_name: {c.name for c in t.columns} for t in spec.tables
    }
    for mv in spec.metric_views:
        src_cols = table_columns_map.get(mv.source_table, set())
        if not src_cols:
            raise ValueError(
                f"Metric view '{mv.view_name}' references source table "
                f"'{mv.source_table}' which does not exist"
            )
        for dim in mv.dimensions:
            dim_expr = dim.get("expr", "")
            # If it's a bare column name, check it exists
            if dim_expr and re.match(r'^[a-z_][a-z0-9_]*$', dim_expr):
                if dim_expr not in src_cols:
                    raise ValueError(
                        f"Metric view '{mv.view_name}' dimension '{dim['name']}' "
                        f"references column '{dim_expr}' not in table '{mv.source_table}'. "
                        f"Available: {sorted(src_cols)}"
                    )
        for m in mv.measures:
            expr_upper = m.get("expr", "").upper()
            if any(kw in expr_upper for kw in ("SELECT ", "FROM ", " JOIN ")):
                raise ValueError(
                    f"Metric view '{mv.view_name}' measure '{m.get('name')}' "
                    f"contains a subquery. Measures must be single aggregate expressions."
                )

    for table in spec.tables:
        if not table.columns:
            raise ValueError(f"Table {table.table_name} has no columns")
        if not table.dimension_values:
            raise ValueError(f"Table {table.table_name} has no dimension_values")


def _validate_generation_expressions(spec: DomainSpec) -> None:
    """Validate that generation_expr values only reference available columns.

    Raises ValueError if any expression references invalid columns or uses
    forbidden aggregate functions, which triggers an LLM retry.
    """
    _FORBIDDEN_AGGS = ("SUM(", "COUNT(", "AVG(", "MIN(", "MAX(", "STDDEV(", "VARIANCE(")
    # Columns always available in the skeleton/filtered CTE
    _SKELETON_COLS = {"dt", "mo", "qty_noise", "status_noise", "select_noise", "id_seq"}

    errors: list[str] = []

    for table in spec.tables:
        entity_fields: set[str] = set()
        if table.dimension_values:
            entity_fields = set(table.dimension_values[0].keys())

        valid_cols = _SKELETON_COLS | entity_fields

        for col in table.columns:
            expr = col.generation_expr
            if not expr:
                # Empty expr means use entity field directly
                if col.name not in entity_fields:
                    errors.append(
                        f"Table '{table.table_name}', column '{col.name}': "
                        f"empty generation_expr but '{col.name}' is not an entity field. "
                        f"Entity fields: {sorted(entity_fields)}"
                    )
                continue

            # Simulate the placeholder substitution done in data.py
            test_expr = expr.upper()
            test_expr = test_expr.replace("{QTY_NOISE}", "QTY_NOISE")
            test_expr = test_expr.replace("{STATUS_NOISE}", "STATUS_NOISE")
            test_expr = test_expr.replace("{SELECT_NOISE}", "SELECT_NOISE")
            test_expr = test_expr.replace("{ID_SEQ}", "ID_SEQ")

            # Check for forbidden aggregate functions
            for agg in _FORBIDDEN_AGGS:
                if agg in test_expr:
                    errors.append(
                        f"Table '{table.table_name}', column '{col.name}': "
                        f"generation_expr uses forbidden aggregate '{agg.rstrip('(')}'. "
                        f"Expressions run per-row and cannot use aggregates. "
                        f"Expression: {expr}"
                    )

            # Validate column references — extract identifiers from the
            # expression and check that they exist in the skeleton CTE.
            # Strip e./d. prefixes, placeholders, and string literals first.
            ref_expr = expr
            ref_expr = re.sub(r"'(?:''|[^'])*'", "", ref_expr)  # strip strings
            ref_expr = re.sub(r"\{[a-z_]+\}", "", ref_expr)  # strip {placeholders}
            # Extract e.XXX and d.XXX references
            aliased_refs = re.findall(r'\b[ed]\.([a-z_][a-z0-9_]*)', ref_expr, re.IGNORECASE)
            for ref in aliased_refs:
                ref_lower = ref.lower()
                if ref_lower not in valid_cols:
                    errors.append(
                        f"Table '{table.table_name}', column '{col.name}': "
                        f"generation_expr references '{ref}' which is not a valid "
                        f"column. Available: {sorted(valid_cols)}. "
                        f"Expression: {expr}"
                    )

        # Check for forward references to other output columns in the same table.
        # In SQL SELECT, a column alias cannot reference another alias from the same SELECT.
        all_output_names = {c.name.lower() for c in table.columns}
        # Output column names that are NOT skeleton/entity columns (i.e., computed columns)
        computed_names = all_output_names - valid_cols

        for col in table.columns:
            expr = col.generation_expr
            if not expr:
                continue
            # Strip string literals and check for bare references to other computed columns
            stripped = re.sub(r"'(?:''|[^'])*'", "", expr)
            stripped = re.sub(r"\{[a-z_]+\}", "", stripped)
            # Find bare identifiers (not prefixed by e. or d.)
            bare_ids = set(re.findall(r'(?<![.a-z_])\b([a-z_][a-z0-9_]*)\b', stripped, re.IGNORECASE))
            # Remove SQL keywords and known functions
            _SQL_NOISE = {"case", "when", "then", "else", "end", "and", "or", "not", "in",
                          "as", "is", "null", "true", "false", "cast", "round", "abs",
                          "concat", "lpad", "substring", "trim", "upper", "lower",
                          "coalesce", "nullif", "greatest", "least", "if", "iff",
                          "date_format", "date_trunc", "month", "dayofweek", "row_number",
                          "over", "order", "by", "string", "int", "double", "decimal",
                          "between", "like", "select", "from", "where", "distinct",
                          "filter", "asc", "desc", "rows", "preceding", "following",
                          "current", "row", "unbounded"}
            bare_ids -= _SQL_NOISE
            bare_ids -= valid_cols  # skeleton + entity fields are valid
            bare_ids -= {col.name.lower()}  # a column can reference itself conceptually (shouldn't but not a forward ref)
            # Check if any remaining bare id is a computed output column name
            forward_refs = bare_ids & computed_names
            if forward_refs:
                errors.append(
                    f"Table '{table.table_name}', column '{col.name}': "
                    f"expression references other computed column(s) {sorted(forward_refs)} "
                    f"which is a forward reference (not available in the same SELECT). "
                    f"Use the underlying noise/entity columns instead. Expression: {expr}"
                )

    if errors:
        raise ValueError(
            "Invalid generation expressions (will retry with LLM):\n"
            + "\n".join(f"  - {e}" for e in errors)
        )
