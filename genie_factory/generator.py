# ---------------------------------------------------------------------------
# Domain spec dataclasses
# ---------------------------------------------------------------------------


@dataclass
class ColumnSpec:
    name: str
    sql_type: str
    comment: str
    is_dimension: bool
    generation_expr: str


@dataclass
class TableSpec:
    table_name: str
    description: str
    columns: list[ColumnSpec]
    seasonal_patterns: dict
    entity_dimension: str
    dimension_values: list[dict]
    category_distribution: dict


@dataclass
class MetricViewSpec:
    view_name: str
    source_table: str
    dimensions: list[dict]
    measures: list[dict]


@dataclass
class ExampleSQL:
    question: str
    sql_lines: list[str]


@dataclass
class SQLSnippets:
    filters: list[dict]
    expressions: list[dict]
    measures: list[dict]


@dataclass
class BenchmarkSpec:
    question: str
    sql_lines: list[str]


@dataclass
class DomainSpec:
    """Complete domain specification generated by LLM."""

    company_name: str
    industry: str
    use_case: str
    space_title: str
    space_description: str
    schema_basename: str

    tables: list[TableSpec]
    metric_views: list[MetricViewSpec]

    genie_instructions: str
    sample_questions: list[str]
    example_sqls: list[ExampleSQL]
    sql_snippets: SQLSnippets
    benchmarks: list[BenchmarkSpec]

    def to_dict(self) -> dict:
        """Serialize to a plain dict suitable for JSON storage."""
        return asdict(self)

    @classmethod
    def from_dict(cls, d: dict) -> "DomainSpec":
        """Reconstruct a DomainSpec from a plain dict (e.g. loaded from JSON)."""
        return cls(
            company_name=d["company_name"],
            industry=d["industry"],
            use_case=d["use_case"],
            space_title=d["space_title"],
            space_description=d["space_description"],
            schema_basename=d["schema_basename"],
            tables=[
                TableSpec(
                    table_name=t["table_name"],
                    description=t["description"],
                    columns=[ColumnSpec(**c) for c in t["columns"]],
                    seasonal_patterns=t.get("seasonal_patterns", {}),
                    entity_dimension=t.get("entity_dimension", "transaction"),
                    dimension_values=t.get("dimension_values", []),
                    category_distribution=t.get("category_distribution", {}),
                )
                for t in d["tables"]
            ],
            metric_views=[MetricViewSpec(**mv) for mv in d["metric_views"]],
            genie_instructions=d["genie_instructions"],
            sample_questions=d["sample_questions"],
            example_sqls=[ExampleSQL(**e) for e in d["example_sqls"]],
            sql_snippets=SQLSnippets(**d["sql_snippets"]),
            benchmarks=[BenchmarkSpec(**b) for b in d["benchmarks"]],
        )


